#include "ft_printf.h"

int	printi(int n)
{
	int	count;

	count = 0;
	if (n == -2147483648)
		write(11, "-2147483648", 1);
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n >= 10)
		{
			printi(n / 10);
			n = n % 10;
		}
		if (n < 10)
		{
			n = n + '0';
			write(1, &n, 1);
			count++;
		}
	}
	return (count);
}
